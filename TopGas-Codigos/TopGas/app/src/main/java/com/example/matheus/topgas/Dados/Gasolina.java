package com.example.matheus.topgas.Dados;

public class Gasolina extends Posto {

    public String apecto;
    public String cor;
    public String teorAlcoolico;
    public String composicaoQuimica;
    public String destilacao;
    public String octanagem;
    public String descricao;
    public String valor;
    public String posto;

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }

    public String getApecto() {

        return apecto;
    }

    public void setApecto(String apecto) {
        this.apecto = apecto;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public String getTeorAlcoolico() {
        return teorAlcoolico;
    }

    public void setTeorAlcoolico(String teorAlcoolico) {
        this.teorAlcoolico = teorAlcoolico;
    }

    public String getComposicaoQuimica() {
        return composicaoQuimica;
    }

    public void setComposicaoQuimica(String composicaoQuimica) {
        this.composicaoQuimica = composicaoQuimica;
    }

    public String getDestilacao() {
        return destilacao;
    }

    public void setDestilacao(String destilacao) {
        this.destilacao = destilacao;
    }

    public String getOctanagem() {
        return octanagem;
    }

    public void setOctanagem(String octanagem) {
        this.octanagem = octanagem;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public String getPosto() {
        return posto;
    }

    public void setPosto(String posto) {
        this.posto = posto;
    }
}
